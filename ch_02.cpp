#include<iostream>
using namespace std;

//?arithmetic opertor

int main(){

    int a = 5;
    int b = 6;

    cout << "The a+b is :" << a + b <<endl;
    cout << "The a-b is :" << a - b<< endl;
    cout << "The a*b is :" << a * b<< endl;
    cout << "The a/b is :" << a / b<< endl;
    cout << "The a%b is :" << a % b << endl;
    cout << "The a++ is :" << a++ << endl;
    cout << "The a-- is :" << a-- << endl;
    cout << "The ++a is :" << ++a << endl;
    cout << "The --a is :" << --a << endl;

    return 0;
}
